ripe = input("Enter your Banana color from those colors only: \n Yellow, Green, Brown \n")


if ripe == "Green":
    print("Banana is Unripe")
elif ripe == "Yellow":
    print("Banana is Ripe")
elif ripe == "Brown":
    print("Banana is Overripe")
else: 
    print("Check your color again")    
    





